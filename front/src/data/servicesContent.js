// Joriy til uchun xizmat ma'lumotini qaytaradi (slug + til bo'yicha kontent).
export function findService(slug, locale = 'uz') {
  const s = servicesContent.find((x) => x.slug === slug)
  if (!s) return null
  const loc = s[locale] || s.uz || s.ru
  return { slug: s.slug, icon: s.icon, ...loc }
}

// Barcha xizmatlarning qisqa ro'yxati (slug + sarlavha) — yon panel uchun.
export function listServices(locale = 'uz') {
  return servicesContent.map((s) => ({
    slug: s.slug,
    icon: s.icon,
    title: (s[locale] || s.uz).title,
  }))
}
